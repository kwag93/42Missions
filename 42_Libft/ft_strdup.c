#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*new;
	int		len;
	int		i;

	len = ft_strlen(str);
	if (!(new = (char *)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	i = 0;
	while (str[i])
	{
		new[i] = str[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
