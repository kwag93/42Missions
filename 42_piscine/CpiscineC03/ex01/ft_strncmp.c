int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned char c1;
	unsigned char c2;

	while (n--)
	{
		c1 = *s1++;
		c2 = *s2++;
		if (c1 != c2)
		{
			if (c1 < c2)
				return (-1);
			else
				return (1);
		}
		if (!c1)
			break ;
	}
	return (0);
}
