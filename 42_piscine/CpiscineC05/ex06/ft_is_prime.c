int		ft_is_prime(int nb)
{
	long long int	i;
	int				result;

    if (nb < 0)
        return 0;
	i = 2;
	if (nb == 0 || nb == 1)
		return (0);
	while (i * i <= nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
